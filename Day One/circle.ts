const circleRadius: number = 5
const phi: number = 3.14

const circleDiameter: number = circleRadius*2
const circleCircumference: number = 2*phi * (circleRadius)
const circleArea: number = phi * (circleRadius**2)

console.log(circleDiameter)
console.log(circleCircumference.toFixed(5))
console.log(circleArea.toFixed(3))

